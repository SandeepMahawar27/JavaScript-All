// basic comparisons
console.log(2 > 1);
console.log(2 >= 1);
console.log(2 < 1);
console.log(2 == 1);
console.log(2 != 1);

// Strings conversions

console.log("2" > 1)
console.log("02" > 1)

// some others conversions

console.log(null > 0)
console.log(null == 0)
console.log(null >= 0)
console.log(null < 0)


console.log(undefined == 0)
console.log(undefined > 0)
console.log(undefined < 0)


// === (strictly check )
console.log("2" === 2);


